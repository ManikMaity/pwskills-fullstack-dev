// Q2. Create a JavaScript function called generateGreeting that takes a name as an argument and returns a 
// personalized greeting message. Use this function to greet three different people.

function generateGreeting (name) {
    return `Hello ${name}, Welcome to PW Skills`;
}
console.log(generateGreeting("Manik")); // Hello Manik, Welcome to PW Skills
console.log(generateGreeting("Suman")); // Hello Suman, Welcome to PW Skills
console.log(generateGreeting("Priya")); // Hello Priya, Welcome to PW Skills
